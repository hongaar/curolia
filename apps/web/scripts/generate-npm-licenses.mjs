#!/usr/bin/env node
import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(webRoot, "../..");
const outDir = path.join(webRoot, "src", "generated");
const outPath = path.join(outDir, "npm-licenses.gen.ts");

/** @param {unknown} node @param {string | undefined} name */
function collectPackageKeys(node, name) {
  /** @type {Set<string>} */
  const keys = new Set();
  /** @param {unknown} n @param {string | undefined} pkgName */
  function walk(n, pkgName) {
    if (!n || typeof n !== "object") return;
    const o =
      /** @type {{ version?: string; dependencies?: Record<string, unknown> }} */ (
        n
      );
    if (pkgName && typeof o.version === "string") {
      keys.add(`${pkgName}@${o.version}`);
    }
    for (const [depName, dep] of Object.entries(o.dependencies ?? {})) {
      walk(dep, depName);
    }
  }
  walk(node, name);
  return keys;
}

/** @param {string | undefined} raw */
function normalizeRepository(raw) {
  if (!raw || typeof raw !== "string") return undefined;
  const trimmed = raw.trim();
  if (!trimmed) return undefined;
  if (trimmed.startsWith("http://") || trimmed.startsWith("https://")) {
    return trimmed;
  }
  if (trimmed.startsWith("git@github.com:")) {
    return `https://github.com/${trimmed.slice("git@github.com:".length).replace(/\.git$/, "")}`;
  }
  if (trimmed.startsWith("git+https://")) {
    return trimmed.replace(/^git\+/, "").replace(/\.git$/, "");
  }
  if (trimmed.startsWith("github:")) {
    return `https://github.com/${trimmed.slice("github:".length)}`;
  }
  return undefined;
}

const lsJson = execSync("npm ls --all --json -w @curolia/web --omit=dev", {
  cwd: repoRoot,
  encoding: "utf8",
  stdio: ["ignore", "pipe", "ignore"],
});

const tree = JSON.parse(lsJson);
const webNode = tree.dependencies?.["@curolia/web"];
if (!webNode) {
  console.error(
    "generate-npm-licenses: @curolia/web not found in npm ls output",
  );
  process.exit(1);
}

const packageKeys = collectPackageKeys(webNode, "@curolia/web");
packageKeys.delete("@curolia/web@0.0.0");

const checkerBin = path.join(
  repoRoot,
  "node_modules",
  ".bin",
  "license-checker",
);
const checkerJson = execSync(
  `"${checkerBin}" --json --excludePrivatePackages`,
  {
    cwd: repoRoot,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "ignore"],
  },
);

/** @type {Record<string, { licenses?: string; repository?: string; licenseFile?: string }>} */
const checker = JSON.parse(checkerJson);

/** @type {{ name: string; version: string; licenses: string; repository?: string }[]} */
const entries = [];

for (const key of packageKeys) {
  const meta = checker[key];
  if (!meta) continue;
  const at = key.lastIndexOf("@");
  if (at <= 0) continue;
  const name = key.slice(0, at);
  const version = key.slice(at + 1);
  const licenses =
    typeof meta.licenses === "string" && meta.licenses.trim()
      ? meta.licenses.trim()
      : "UNKNOWN";
  const repository = normalizeRepository(meta.repository);
  entries.push({
    name,
    version,
    licenses,
    ...(repository ? { repository } : {}),
  });
}

entries.sort((a, b) => {
  const byName = a.name.localeCompare(b.name);
  if (byName !== 0) return byName;
  return a.version.localeCompare(b.version);
});

fs.mkdirSync(outDir, { recursive: true });

const source = `// Auto-generated by apps/web/scripts/generate-npm-licenses.mjs
// Do not edit manually.

export type NpmLicenseEntry = {
  name: string;
  version: string;
  licenses: string;
  repository?: string;
};

export const NPM_LICENSE_ENTRIES: NpmLicenseEntry[] = ${JSON.stringify(entries, null, 2)};
`;

fs.writeFileSync(outPath, source, "utf8");
console.log(
  `Wrote ${entries.length} npm licence entries to ${path.relative(repoRoot, outPath)}`,
);
